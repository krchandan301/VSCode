// switch statements

const grade = "D";
switch (grade) {
    case "A" :
        console.log("Your grade is A");
        break;
    case "B" :
        console.log("Your grade is B");
        break;
    case "C" :
        console.log("Your grade is C");
        break;
    case "D" :
        console.log("Your grade is D");
        break;
    case "E" :
        console.log("Your grade is E");
        break;
    default:
            console.log("Not a valid grade.");
        break;
}


